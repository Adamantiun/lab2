public class Position {
    private int x;
    private int y;
    public Position(int xi, int yi){
        x = xi;
        y = yi;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int nx){
        x = nx;
    }
    public void setY(int ny){
        y = ny;
    }
}
